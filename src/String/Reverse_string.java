package String;

public class Reverse_string {
	
		public static void main(String[] args) {
			

			String s="abcde";
			System.out.println("Original string:"+s);
			System.out.println(s.length());
			
			for (int i= s.length()-1; i >=0 ; i--) {
				
				System.out.print(s.charAt(i));
				
			}
		}	
		}
	
	

