package Patterns;

public class Sample3 {
	public static void main(String[] args) {
		
		//* * *
		//* *
		//*
		
		
		int col=3;                                 
		for (int i = 1; i <=3; i++) {
			for (int j = 1; j <=col; j++) {     
				System.out.print("* ");
				
			}
			 System.out.println();
			col--;
		}
		
		
		
	}

}
